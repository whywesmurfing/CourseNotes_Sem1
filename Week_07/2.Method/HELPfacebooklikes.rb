def facebook_likes(array)
    if array.length == 0
        puts "No one likes this"
    elsif array.length == 1
        puts "#{array[0]} likes this"
    elsif array.length == 2
        puts "#{array[0]} and #{array[1]} likes this"
    else
        puts "#{array[0]}, #{array[1]} and " + (array.length - 2).to_s + " others likes this"
    end    
end

